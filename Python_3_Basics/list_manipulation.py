x = [1, 2, 3, 4, 5]
y = ['Janet', 'Jessy', 'Bob', 'Adam', 'Chris', 'George', 'Ema']

# append
x.append(6)
print(x)

# insert
x.insert(6, 7)
print(x)

# remove
x.remove(6)
print(x)

# slice
print(x[0:2])
print(x[2:4])

# last element
print(x[-1])

# index
print(x.index(1))

# count
print(x.count(1))

# sort
x.sort()
print(x)
y.sort()
print(y)
