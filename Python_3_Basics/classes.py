class calculator:

    def addition(x, y):
        added = x + y
        print(added)

    def substraction(x, y):
        substracted = x - y
        print(substracted)

    def multiplication(x, y):
        multiplied = x * y
        print(multiplied)

    def division(x, y):
        divided = x / y
        print(divided)

calculator.addition(1, 2)
calculator.substraction(1, 2)
calculator.multiplication(1, 2)
calculator.division(1, 2)
